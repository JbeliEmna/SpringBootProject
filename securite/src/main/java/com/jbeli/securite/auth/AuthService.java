    public String register(RegisterRequest request){
        boolean isValidEmail = validator.test(request.getEmail());
        if(!isValidEmail){
            throw new IllegalStateException("email n'est pas valide");
        }
        RoleEnum role  = validator.testRole(String.valueOf(request.getRole()));
        String token = userService.signUpUser(new User(
                request.getNom(),
                request.getEmail(),
                request.getMotDePasse(),
                request.getRole()
        ));
        String link = "http://localhost:8080/api/v1/registration/confirm?token=" + token;
        emailSender.send(request.getEmail(),buildEmail(request.getNom(), link));
        return token;
    }

    public AuthResponse authenticate(AuthRequest request) {
        Authentication authenticatedUser = authManager.authenticate(
                new UsernamePasswordAuthenticationToken(
                        request.getEmail(),
                        request.getMotDePasse()
                )
        );
        var userDetails = (User) authenticatedUser.getPrincipal();
        HashMap<String, Object> claims = new HashMap<>();
        var authorities = userDetails.getAuthorities()
                .stream()
                .map(GrantedAuthority::getAuthority)
                .toList();
        claims.put("firstname", userDetails.getNom());
        claims.put("authorities", authorities);
        var jwtToken = jwtService.generateToken(claims, userDetails);
        return AuthResponse.builder()
                .token(jwtToken)
                .build();
    }


package com.jbeli.evaluation.interview;


import com.jbeli.evaluation.test.Test;
import com.jbeli.gestion.utilisateurs.candidat.Candidat;

import io.swagger.annotations.ApiModel;
import io.swagger.annotations.ApiModelProperty;
import jakarta.persistence.*;
import lombok.*;
import org.springframework.data.annotation.CreatedDate;

import java.util.*;

@Getter
@Setter
@AllArgsConstructor
@NoArgsConstructor
@Builder
@Entity
@ApiModel("Contains all the attributes required under Interview entity")
public class Interview {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @ApiModelProperty("This the unique ID of the interview , will be generated by the system")
    private Integer id ;

    @ApiModelProperty("start date of the interview")
    private Date dateDebut;

    @ApiModelProperty("end date of the interview")
    private Date dateFin;

    @OneToMany(mappedBy = "interview",  orphanRemoval = true)
    private List<Test> tests = new ArrayList<>();

    @Getter
    private List<Candidat> candidats;

    public List<Candidat> getCandidat() {
        return candidats;
    }



}

package com.jbeli.evaluation.pieceJointe;


import com.jbeli.evaluation.test.Test;
import io.swagger.annotations.ApiModel;
import io.swagger.annotations.ApiModelProperty;
import jakarta.persistence.*;
import jakarta.validation.constraints.NotBlank;
import jakarta.validation.constraints.NotEmpty;
import lombok.*;
import org.springframework.data.annotation.CreatedDate;

import java.util.Date;

@Getter
@Setter
@AllArgsConstructor
@NoArgsConstructor
@Builder
@Entity
@ApiModel("Contains all the attributes required under Attachment entity")
public class PieceJointe {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @ApiModelProperty("This the unique ID of the Attachment, will be generated by the system")
    private Integer id ;

    @NotBlank
    @NotEmpty
    @ApiModelProperty("This the subject of the Test ")
    private String sujet;

    @OneToOne
    @JoinColumn(name = "test_id")
    private Test test;

}
